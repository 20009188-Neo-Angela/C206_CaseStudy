public class LunchBox {
	
	private String meal;
	private String drink;
	private String fruit;
	private String day;
	private String date;
	
	public LunchBox(String meal, String drink, String fruit) {
		
		this.meal = meal;
		this.drink = drink;
		this.fruit = fruit;
		
	}
	
	public String getMeal() {
		return meal;
	}

	public String getDrink() {
		return drink;
	}

	public String getFruit() {
		return fruit;
	}

}
